import * as types from './mutations-types'
const mutations={
     /**
      * state:当前状态树
      * v: 提交mutation时传的参数
      */
  [types.SET_USERINFOS] (state, userInfos) {
    state.userInfos = userInfos;
  },
  [types.SET_TOKEN] (state, token) {
    state.token = token;
  },
  [types.SET_BOXFLAG] (state, flag) {
    state.boxflag = flag;
  },
  [types.SET_FIRSTFLAG] (state, flag) {
    state.firstflag = flag;
  },
  [types.SET_TWOFLAG] (state, flag) {
    state.twoflag = flag;
  },
  [types.SET_THREEFLAG] (state, flag) {
    state.threeflag = flag;
  }
}

export default mutations
